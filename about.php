             <h3 class="heading-tertiary u-margin-bottom-small">childhood disaster</h3>
             <p class="paragraph">
                 Mary Anning was born on 21 May 1799. She lived in the English seaside own of Lyme Regis in Dorset. When she was 8 months old, her mum and two other women were holding her outside, but then thunderstruck and they rushed under a tree to protect the baby. They were fine until lightning hit the tree, leaving the three women dead but Mary alive. People thought the lightning changed her personality, as before it happened she was dull but after it happened she was bright and bubbly.




             </p>

             <h3 class="heading-tertiary u-margin-bottom-small">about mary</h3>
             <p class="paragraph textSize ">
                 Due to the fact Mary's family was poor, she went out with her father and her brother to go find things she could sell on the beach. Mary was only really interested in finding 'curiosities' -fossils- to sell. One day her father was climbing to find fossils. Though he survived, he suffered many injuries.<br>
                 When she was just 12, she found a massive piece poking out from a cliff. They thought it might be a crocodile, but what she had discovered was an ancient reptile called an ichthyosaur (which means ‘fish lizard’). Mary went on to make more incredible discoveries in her life, including a long-necked marine reptile, called a plesiosaur and a flying reptile called a Dimorphous.
             </p>

